package ch11Test2;

public class Bus {
	int busNumber;
	int money;
	int passerger;
	
	public Bus(int busNumber) {
		this.busNumber = busNumber;
	}
	
	public void take(int money) {
		this.money += money;
		passerger++;
	}
	
	public void showBusInfo() {
		System.out.println(busNumber+"번 버스의 승객은 "+passerger+"명 이고 수익은 "+money+"입니다.");
	}
	
}
